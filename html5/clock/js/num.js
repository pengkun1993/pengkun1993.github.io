numObj.prototype.update = function() {
    var the_num=digit[this.num];
    var the_w=this.w/7;
    var the_h=this.h/10;
    var ball_r=Math.min(the_w,the_h)*0.4;
    var allballobj=[];
    var k=0;
    for(var i=0;i<the_num.length;i++){
        for(var j=0;j<the_num[i].length;j++){
            if(the_num[i][j]==1){
                var ball_x=this.x+(j+0.5)*the_w;
                var ball_y=this.y+(i+0.5)*the_h;
                var ball_vx=(Math.random()-0.7)*5;
                var ball_vy=(Math.random()-1)*3+1;
                var cR=Math.round(Math.random()*255);
                var cG=Math.round(Math.random()*255);
                var cB=Math.round(Math.random()*255);
                var ball_color='rgb('+cR+','+cG+','+cB+')';
                var oball=new ballObj(ball_x,ball_y,ball_r,ball_vx,ball_vy,ball_color);
                allballobj[k++]=oball;
            };
        }
    }
    return allballobj;
};